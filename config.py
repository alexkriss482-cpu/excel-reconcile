def _normalize_header(value: object) -> str:
    text = str(value).strip().lower()
    text = re.sub(r"\s+", " ", text)
    return text


def find_column(columns: Iterable[object], aliases: list[str]) -> str | None:
    """Найти имя колонки DataFrame по списку возможных заголовков.

    Первый alias в списке имеет наивысший приоритет.
    """
    normalized_columns = {_normalize_header(column): str(column) for column in columns}
    for alias in aliases:
        match = normalized_columns.get(_normalize_header(alias))
        if match is not None:
            return match
    return None


def _apply_department_rules(text: str) -> str:
    if text in DEPARTMENT_MERGE_OVERRIDES:
        return DEPARTMENT_MERGE_OVERRIDES[text]

    for suffix in DEPARTMENT_SUFFIXES_TO_STRIP:
        if text.endswith(suffix):
            text = text[: -len(suffix)].strip()
            break

    return DEPARTMENT_ALIASES.get(text, text)


def normalize_department(name: object) -> str | None:
    """Объединить подразделения и вернуть нормализованное имя."""
    if name is None or (isinstance(name, float) and str(name) == "nan"):
        return None

    text = str(name).strip()
    if not text:
        return None

    for pattern, replacement in REGISTRY_DEPARTMENT_PATTERNS:
        match = re.match(pattern, text, flags=re.IGNORECASE)
        if match:
            text = match.expand(replacement).strip()
            break

    return _apply_department_rules(text)
